# –ù–∞–π–¥–µ–º –¥–æ–≤–µ—Ä–∏—Ç–µ–ª—å–Ω—ã–π –∏–Ω—Ç–µ—Ä–≤–∞–ª –¥–ª—è —Å—Ä–µ–¥–Ω–∏—Ö –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–∏—Ö –ø–æ —Ñ–æ—Ä–º—É–ª–µ ùë•¬†ÃÖ¬±ùë°_(ùõº/2) * ùúé/‚àöùëõ, —Ç.–∫. 
# –°–ö–û - –≥–µ–Ω–µ—Ä–∞–ª—å–Ω–æ–π —Å–æ–≤–æ–∫—É–ø–Ω–æ—Å—Ç–∏ –Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–æ
import scipy.stats as stats
import numpy as np
data = [6.9, 6.1, 6.2, 6.8, 7.5, 6.3, 6.4, 6.9, 6.7, 6.1]
data.sort()
a = 1- 0.95
n=10
sigma = np.std(data, ddof=1)
x = np.mean(data)
t = stats.t.ppf( a / 2, n-1)
print(f"–î–æ–≤–µ—Ä–∏—Ç–µ–ª—å–Ω—ã–π –∏–Ω—Ç–µ—Ä–≤–∞–ª –¥–ª—è —Å—Ä–µ–¥–Ω–∏—Ö –∞—Ä–µ—Ñ–º–µ—Ç–∏—á–µ—Å–∫–∏—Ö {x+t*sigma/np.sqrt(n):.2f} {x-t*sigma/np.sqrt(n):.2f}")